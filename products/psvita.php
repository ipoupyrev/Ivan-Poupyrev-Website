<h2>History and the current state.</h2>
<p><a href="http://www.ivanpoupyrev.com/projects/12Pixels.php">12Pixels</a> was developed  at <a href="http://www.sonycsl.co.jp/en/">Sony CSL</a> by Karl Willis and myself  in  2007 - 2008. At that time all phone interaction, particularly in Japan, was based only on key presses. Smart phones were nonexistent and he touch screen interaction was virtually unknown. Sony Ericsson was  one of the major manufacturers of mobile phones that were popular in Europe, Asia and Middle East, i.e. in countries with GSM network.</p>
<p align="center"><a href="cedar/se-logo-large.jpg"><img src="cedar/se-logo.jpg"></a></p>
<p>With the release of iPhone  in late 2007 touch screen-based smart phones rapidly took off. The traditional &quot;candybar&quot;  feature phones were still quite popular  due their to low price and long battery life. However, it soon became clear that smart phones were the future of movile phones and not much research or development was  put into the feature phones. in 2012 Sony Ericsson was folded into Sony becoming Sony Mobile Communication, the production of traditional feature  phones was abandoned all together with company focusing on smart phones market segment instead. <br>
</p>
