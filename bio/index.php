   <h2><a name="sbio" id="books"></a><a href="#sbio">Biography.</a> </h2>
   <p>Dr. Ivan Poupyrev directs an Interaction Technology group in Disney Research's Pittsburgh Lab, Walt Disney Imagineering, a unit of Walt Disney  Company tasked with dreaming up and developing future technologies for Disney parks, resorts, and cruises.    
   <p>Dr. Poupyrev's research focuses on inventing new interactive technologies for the seamless blending of digital and physical properties in devices, everyday objects, and living environments, a direction he broadly refers to as physical computing. It's span a broad range of research fields including haptic user interfaces, tangible interfaces, shape-changing and flexible computers, augmented and virtual reality as well as spatial 3D interaction. His research was broadly published and received awards at prestigious academic conferences, exhibited world-wide and extensively reported in popular media, such as CNN, BBC, Financial Times, New York Times, New Scientist to name a few. The results of his research were also released on the market in various consumer products and applications.    
   <p>Prior to Disney, Ivan has worked as a researcher at Sony Computer Science Laboratories and the Advanced Telecommunication Research Institute International, both in Japan. He also had a stint at the Human Interface Technology Laboratory at the University of Washington as a Visiting Scientist while working on his Ph.D. dissertation in Hiroshima University, Japan.

