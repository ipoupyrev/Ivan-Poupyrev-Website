<a href="moments.php">Recording sound with Special Moments.</a></h2>
<p>Special Moments proposes a new category of casual sound recording devices designed as everyday objects  that can be used effectively without pressing buttons or selecting items from a menu.  Instead they rely on embodied and gestural interaction to seamlessly blend sound recording into everyday user activities [<a href="moments.php"><strong>more details...</strong></a>].</p>
<p>Special Moment  is a collaborative project with<strong> Haruo Oba</strong> and <strong>Takuo Ikeda</strong> both from Sony Corporation. It was presented at <a href="../publications/index.php#12pix">CHI 2008</a> conference and reported in a prototype issue of <a href="../media/index.php#axis-sm">Axis</a> magazine.</p>

