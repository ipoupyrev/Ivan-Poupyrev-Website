<h2><a href="touche.php">Touch&eacute;: Touch Sensing for the Real World.</a></h2>
<p><em>Touch&eacute;</em> is a new touch  sensing technology that can not only  detect a single touch,  but simultaneously recognize complex  configurations of the human hands  and body.  Instrumenting the world with <em>Touch&eacute;</em> is easy: a single wire is sufficient to make objects and  environments both touch and gesture sensitive. In our studies we added  complex touch and gesture sensitivity not  only to computing devices and  everyday objects, such as a door knob, but also to the human body and liquids [<a href="touche.php"><strong>more details ...</strong></a>].</p>


