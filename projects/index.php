<h2>
<a href="motionbeam.php">MotionBeam: Interactive Mobile Projectors.</a></h2>
<p>We explores the use of mobile projectors to  interact and control projected characters in gaming and storytelling. Physical movement of the  projector, much like a motion controller, is used to guide and  interact with the character. This creates a unified interaction style  where sensor input and projector output are tied together within a  single device [<a href="motionbeam.php"><strong>more details...</strong></a>].</p>

