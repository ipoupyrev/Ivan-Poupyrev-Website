<h2><a href="shaptics.php">Surround Haptics and Tactile Brush.</a></h2>
<p>We developed a new tactile technology that uses low-resolution grids of vibrating actuators to create high-resolution,  continuous moving tactile strokes on human skin. Our technology is based on   modeling and controlling tactile illusions and it can be used to design novel compelling tactile experiences for computer games, rides and park attractions, devices for blind and interfaces for vehicle control [<a href="shaptics.php"><strong>more details...</strong></a>].</p>

