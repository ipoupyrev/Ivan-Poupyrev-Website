<h2><a href="sidebyside.php">SideBySide: Collaborative Mobile Projectors.</a></h2>
<p><em>SideBySide</em>  lets   people  play  together using handheld projectors  anytime and anywhere. The interaction is immediate and simple: users  project  onto any surface and their projections become aware and responsive to  other projections nearby. The applications of <em>SideBySide</em> can range from    games, such as boxing with projected characters, to everyday tasks, e.g. exchanging personal contact information [<a href="sidebyside.php"><strong>more details ...</strong></a>].</p>

