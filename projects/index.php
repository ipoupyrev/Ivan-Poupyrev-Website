<h2><a name="r_topics"></a><a href="index.php">Projects.</a></h2>

<p>I am interested in creating interfaces and technologies that seamlessly merge  digital and physical  in everyday objects, tools and environments. </p>
<p>The projects  archived below describe some of the modest steps that I took in this direction. They cover work that I did at <a href="http://www.disneyresearch.com/">Disney Research</a>, <a href="http://www.sonycsl.co.jp/en/">Sony Computer Science Labs</a> in Tokyo,<a href="http://www.atr.jp/index_e.html"> Advanced Telecommunicaiton Research Institute </a> in Kyoto, University of Washington in Seattle and Hiroshima University. </p>

