<h2>
<a href="teslatouch.php">TeslaTouch: Touchscreen tactile display.</a></h2>
<p><em>TeslaTouch</em> is a new tactile  feedback technology for touch screens that does not use any mechanical motion. Based on a principle of electrovibration, it provides wide range of tactile  sensations to fingers sliding across a  touch surface. It allows simulating feeling of textures, grids and materials, feel interaction between virtual objects, encode additional information into tactile channel, and add tactile feedback to  interface objects [<a href="teslatouch.php"><strong>more details...</strong></a>].</p>

