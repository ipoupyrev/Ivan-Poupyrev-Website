<a href="12Pixels.php">12 Pixels:  Exploring mobile creativity.</a></h2>
<p>How can we draw on a mobile phone witouht touch scrren? What kind of pictures can a person draw?</p>
<p>12Pixels is an innovative interface, mobile phone application, and web service that allows people to draw and share pixel-based imagery using only the regular twelve keys of the mobile phone. 12Pixels does not require expensive touch screen  interfaces: anyone with a regular mobile phone and internet connection, everywhere from Tokyo to Nairobi, can create pixel-based art and share it with the world [<a href="12Pixels.php"><strong>more details...</strong></a>].</p>
<p>12Pixels  is a collaborative project with <strong><a href="http://www.darcy.co.nz/">Karl D.D. Willis</a></strong>. <a href="http://www.jp.sonystyle.com/">Sony Style</a> of Japan released 12Pixels as a <a href="http://www.jp.sonystyle.com/Taiken/Original/12pixels/Features/index.html">free service</a> for general public in 2009.</p>

