<a href="tactile.php">Tactile user interfaces.</a></h2>
<p> Tactile user interfaces  allow people to perceive information through touch, leading to easier, more natural and enjoyable interaction. 
<p>My work on tactile user interfaces covers a range of projects, including the development of TouchEngine&trade; tactile feedback platform, designing  tactile interfaces for pen interaction and  small touch screens  [<a href="tactile.php"><strong>more details ...</strong></a>]. 
<p>Tactile interfaces research is a collaboration with <strong>Shigeaki Maruyama, </strong>Sony Corporation. It has been extensively <a href="../publications/index.php">published,</a> reported in public <a href="../media/index.php">media</a> and released on on the market in a number of Sony <a href="../products/index.php">products</a>; which were the first consumer electronic products in the world that provided tactile feedback on touch screens.</p>

