<a href="lumen.php">Lumen: A shape-changing display.</a></h2>
<p> Lumen is a concept of an interactive display device that can dynamically
  change its own mechanical shape in order to communicate information to the users [<a href="lumen.php"><strong>more details...</strong></a>].</p>
<p>Lumen is a collaborative project with <strong>Tatsushi Nashida</strong> and <strong>Shigeaki Maruyama, </strong>both from Sony Corporation. It was demonstrated at  <a href="../exhibits/index.htm">SIGGRAPH 2004</a> and <a href="../exhibits/index.htm">ARS Electronica Festival 2005; </a>it was also reported  in the annual prototype issue of the <a href="../media/index.php">Axis</a> magazine. </p>

