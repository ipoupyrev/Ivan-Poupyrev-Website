<h2 align="left">What is SideBySide?</h2>
<p><em>SideBySide</em> is a novel interactive system that allows multiple  people to play and work together using handheld projectors at anytime  and anyplace. The system is immediate and simple: users simply project  onto a surface and their projection becomes aware and responsive to  other projections nearby. Interaction can range from projector-based  games, such as boxing with projected characters, to everyday tasks such  as exchanging contact information by &quot;dragging and dropping&quot; onto  another user's projection.<br>
  </p>
 
<p>Importantly, <em>SideBySide</em> does  not require any fixed sensing in the environment and can be used  anywhere: at home, at the office, or even inside the car during long  road trips. The system consists of a hybrid mobile projector that  outputs both visible and invisible projections at the same time. The  invisible projection contains tracking data that can be recognized by  the device camera, allowing accurate location tracking of multiple  projections and lightweight communication between devices.</p>

