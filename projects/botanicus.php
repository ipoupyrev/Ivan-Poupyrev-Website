<h2 align="left">What is Botanicus Interacticus?</h2>
<p><em>Touch&eacute;</em> is a new touch  sensing technology that can not only detect a single touch event,  but simultaneously recognize complex configurations of the human hands  and body during touch interaction. <em>Touch&eacute;</em> is based on a novel sensing approach that we call <em>Swept Frequency  Capacitive Sensing</em>  which allows to significantly enhance  touch and gesture interaction in a broad variety of applications. For example, in our explorations we added  complex touch and gesture sensitivity not only to computing devices and  everyday objects, such a doorknob or a surface of a desk, but also to the human body and liquids. Importantly,  instrumenting objects and material with <em>Touch&eacute;</em>  is easy: a single wire is sufficient to make objects and  environments both touch and gesture sensitive.<br>
  <br>
  We demonstrate the rich  capabilities of <em>Touch&eacute;</em> with five example setups from different  application domains and conduct experimental studies that show gesture  classification accuracies of 99% are achievable with this technology.</p>

