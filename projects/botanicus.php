<h2>Botanicus Interacticus in Pictures.</h2>
<a href="touche/Touche-Doorknob-Large.jpg"><img src="touche/Touche-Doorknob-450.jpg" width="450" height="257"></a>
<div id="pjimagecap-new">
<p><em>Touch&eacute; </em>is a new sensing technology that allows to make everyday objects, such as a common door knob, touch and gesture sensitive by attaching it to our sensor with just a single wire.</p>
</div>
<a href="touche/Touche-Pinch-Large.jpg"><img src="touche/Touche-Pinch-450.jpg" width="450" height="450"></a>
<div id="pjimagecap-new">
<p><em>Touch&eacute;</em> is low-power and miniature sensor; it can be embedded into small hand held devices and recognize how the human hand is grasping them.</p>
</div>

<a href="touche/Touche-HandGesture-Large.jpg"><img src="touche/Touche-HandGesture-450.jpg" width="450" height="450"></a>
<div id="pjimagecap-new"><em>Touch&eacute;</em> can make human body touch and gesture sensitive and recognize how the user is touching her own body.</div>

<a href="touche/Touche-DRP-Gestures-Large.jpg"><img src="touche/Touche-DRP-Gestures-450.jpg" width="450" height="257"></a>
<div id="pjimagecap-new">
<p><em>Touch&eacute;</em> can sense touch and hand postures on everyday objects, hand grasp  on hand-held devices, human gestures directly on their bodies and with  unusual materials such as water.</p>
</div>


<a href="touche/Touche-Diagram-Large.jpg"><img src="touche/Touche-Diagram-450.jpg" width="450" height="285"></a></p>
<div id="pjimagecap-new">
<p><em>Touch&eacute;</em> is measuring capacitive response of an object and human body at multiple  frequencies, a technique that we called <em>Swept</em> <em>Frequency Capacitive  Sensing</em>. The signal travels through an object or human body via different paths depending on signal  frequency, capturing the posture of human hand and body as well as other  properties of the interaction context. The resulted data is classified using  machine learning algorithms to identify gestures that are then used to  trigger desired responses of the user interface. </p>
</div>


<a href="touche/Touche-Doorknobtouch-Large.jpg"><img src="touche/Touche-Doorknobtouch-450.jpg" width="450" height="253"></a>
<div id="pjimagecap-new">
<p>In one examplatory set up we can measure the configuration of human hand when it is touching a doorknob.</p>
</div>
<a href="touche/Touche-HandheldTouch-Large.jpg"><img src="touche/Touche-HandheldTouch-450.jpg" width="450" height="253"></a>
<div id="pjimagecap-new">
<p>Number of fingers pinching a handheld object can also be recognized.</p>
</div>
<a href="touche/Touche-Bodytouch-Large.jpg"><img src="touche/Touche-Bodytouch-450.jpg" width="450" height="253"></a>
<div id="pjimagecap-new">
<p><em>Touch&eacute;</em> can recognize how the user touches his own body.</p>
</div>

<a href="touche/Touche-Watertouch-Large.jpg"><img src="touche/Touche-Watertouch-450.jpg" width="450" height="253"></a>
<div id="pjimagecap-new">
<p>Our sensing technology allows to measure touche and gesture interactivity on unusual materials such water and other liquids.</p>
</div>

<a href="touche/Touche-DRP-Board-Large.jpg"><img src="touche/Touche-Board-450.jpg" width="450" height="285"></a>
<div id="pjimagecap-new">
<p><em>Touch&eacute;</em> sensing is performed on a custom-made control board, which is  powered by ARM Cortex-M3 120 Mhz microprocessor. The sensor board is  compact, battery powered and equipped with on-board Bluetooth wireless  communication. It can add touch and gesture sensitivity to a broad  variety of objects by using just a single wire connection. Both  stationary and handheld objects could be easily augmented with <em>Touch&eacute;</em>  sensor.</p>
</div>


