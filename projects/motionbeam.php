<h2 align="left">What is MotionBeam?</h2>
<p>The <em>MotionBeam</em> project explores new forms of  interaction using handheld projectors. With our prototype system, users interact and control projected characters by moving and gesturing with the projection device that is enhanced with motion sensing system. The interaction principles are partially based on fundamental principles that we adapted from animation and comic art, such us secondary motion and closure. This creates a unified interaction style where sensor input and projector output are tied together within a single device.
<p>Our character and racing game applications show how MotionBeam can be used with mobile games. It can also be utilized for augmented reality interaction by linking projected content to physical objects in the environment. We envision MotionBeam as a key component in a new 'game projector' platform where the real world becomes a playground and users interact directly with each other and the environment.
