<h2>MotionBeam in Pictures.</h2>
<a href="motionbeam/motionbeam-character-game.jpg"><img src="motionbeam/motionbeam-character-game-450.jpg" width="450" height="253"></a>
<div id="pjimagecap-new">
<p>In the MotionBeam character game the user guides a character by pointing  up, down, left and right with the projector. The user leads the  character along a trail of stars to locate its missing vehicle while avoiding a hostile character.</p>
</div>

<a href="motionbeam/motionbeam-racing-game.jpg"><img src="motionbeam/motionbeam-racing-game-450.jpg" width="450" height="253"></a>
<div id="pjimagecap-new">
<p>In the MotionBeam racing game the user steers the character's vehicle by  angling with the projector. The user must guide the character past a  number of turns and obstacles to reach the end of the track without  falling off.</p>
</div>


<a href="motionbeam/motionbeam-metaphor.png"><img src="motionbeam/motionbeam-metaphor-450.jpg" width="450" height="303"></a>
<div id="pjimagecap-new">
<p>MotionBeam interaction techniques are based on the control of an object attached to the end of a  metaphorical beam.</p>
</div>

<a href="motionbeam/motionbeam-interaction-principles.png"><img src="motionbeam/utsushie-450.jpg" width="450" height="241"></a></p>
<div id="pjimagecap-new">
<p>MotionBeam builds on a rich history of using hand-manipulated projected images in story telling such as <em>utsuhie</em> in Japan.</p>
</div>

<a href="motionbeam/motionbeam-interaction-principles.png"><img src="motionbeam/motionbeam-interaction-principles-450.jpg" width="450" height="373"></a></p>
<div id="pjimagecap-new">
