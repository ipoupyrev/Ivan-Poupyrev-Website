<p>To listen to the recordings, the user plugs headphones into the back of the device, switching it into playback mode. In playback mode, <em>opening</em> the book starts playback of previously recorded data. </p>
<p>To navigate through multiple recordings, the user briefly <em>closes</em> and <em>opens</em> the book. This gesture is interpreted as navigating to the next recording. Closing the book for a longer duration stops playback and resets the device to the first recording. </p>
<p>We envision that each recording device will be inexpensive and used only with one book. Hence the child, once grown up, can re-experience reading the same book with his or her parents.</p>
