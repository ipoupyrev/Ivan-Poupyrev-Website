<h1><a name="candl" id="book2"></a>Candle recorder.</h1>
<p>The candle recorder can be used in the context of a party, dinner, or other similar social occasion. Lighting a candle in such a context forms a natural trigger indicating the beginning of an event to start recording. </p>
<p>The sound recorder is designed as a candle stand. The candlelight is sensed and tracked by an inexpensive infrared light sensor. <em>Lighting</em> up a candle starts the recording, while <em>extinguishing</em> the flame stops the recording. An LED on top of the device indicates that the device is in recording mode.</p>
<br>
<img src="moments/CR_w_and_b_s.jpg" width="450" height="262"><br>
<div id="pjimagecap"> <div align="right">
The candle recorder can both capture and playback recordings.<br>
</div></div>
<p>To play back the recording, the user simply flips the candle stand over and the device becomes a small speaker. To start the playback the user twists the device, and an embedded gyroscope sensor recognizes this gesture and initiates playback of the recording. Twisting the device again will fast forward to the next recording and twisting the device in the opposite direction will stop the playback.</p>
<div id="green_line"></div>
<h1><a name="concl" id="book3"></a>Conclusions.</h1>
 <p>The main theme of this project is exploration of context-dependent and embodied interfaces for media capture. We are especially interested in understanding how such computing devices can be seamlessly embedded into everyday environments, providing fluent and unobtrusive interaction.</p>
<p>More details on the project, including implementation, hardware design and evaluation, can be found in the <a href="../e-library/2008/chi2008_specialmoments.pdf">paper</a> presented at the CHI 2008 conference. </p>
