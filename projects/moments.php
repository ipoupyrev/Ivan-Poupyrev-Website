<h2 align="left"><a name="design" id="design"></a>Design principles.</h2>
<p>Our objects were designed based on the following principles:</p>
<p>1. <em>Context-dependent</em> and <em>context-aware interaction</em>. We surveyed a wide range of human activities and concluded that in everyday life there are very few situations when recorded sound presents value to users. Therefore we designed our devices to be highly context-specific, i.e. they were designed to operate only in those situations where sound recording is desirable. Their typical operations, such as starting or stopping recordings, are triggered by recognizing appropriate changes in the surrounding context.</p>
<p>2. <em>Embodied interaction</em>. We observed that often there are certain signature objects that are key for a specific context, e.g. a candle is an important object at a birthday party or a candlelight dinner. By embedding sensors into these objects we can track their state and estimate context changes, e.g. when the light of the candle is extinguished we can conclude that the candlelight dinner is over. These objects can become natural interface &quot;controls&quot;. Their normal, conventional use can replace generic user interface elements such as buttons and switches. </p>
<p>3. <em>Self-contained sound albums</em>. The devices that we developed have both recording and playback functionality. They can be used without any external devices such as computers, speakers or television sets. We envision users having multiple devices that will work as &quot;sound albums&quot; preserving memories of the events and situations.</p>
<p>We developed two recording devices based on these principles: the children&rsquo;s book recorder and the candle recorder.</p>
