<h2 align="left"><a name="moti" id="moti"></a>What is it?</h2>
<p>In the Special Moments project we propose a new category of casual sound recording devices that can be used to capture and store our everyday experiences in the form of &quot;sound albums&quot;. </p>
<p>Our goal was to design a collection of simple and inexpensive recording objects that would blend into everyday environments and would be used effectively without pressing buttons or selecting items from a menu. In fact, our devices do not have any displays and controls in the traditional sense. Instead they rely on embodied and gestural interaction to seamlessly blend sound recording into everyday user activities.</p>
<p>We implemented two working prototypes: a candle recorder, which captures the general atmosphere at a party, and a book recorder, which records the interactions between parents and children while they are reading a book together.</p>
