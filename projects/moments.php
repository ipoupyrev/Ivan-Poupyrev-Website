<h1><a name="book" id="book"></a>Book recorder.</h1>
<p>A children's book recorder can be used by parents when they read a book together with their children. </p>
<p>The device can be attached to any children&rsquo;s book. It includes a sensor that detects when the book is opened or closed. <em>Opening</em> the book triggers the recording, capturing the interactions between the child and a parent reading the book together. <em>Closing</em> the book stops the recording.</p>
<p>&nbsp; </p>
