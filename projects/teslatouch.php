<h2>TeslaTouch in Pictures.</h2>
<a href="teslatouch/hi-res/tt-basic-large.jpg"><img src="teslatouch/tt-basic.jpg" width="450" height="256"></a>
<div id="pjimagecap-new">
<p>TeslaTouch is based on a principle of <em>electrovibration</em>,  discovered in 50s. We  excite the  electrode with an electrical signal  of sufficient amplitude generating a periodic attractive force  between  sliding finger and the  panel. This creates  the <em>dynamic friction</em> between the finger and the surface of the panel.</p>
</div>
<a href="teslatouch/hi-res/tt-finger-hr.jpg"><img src="teslatouch/tt-finger.png" width="450" height="153"></a>
<div id="pjimagecap-new">
<p>Changes in friction  are  periodic and generate the feeling of dynamic tactile textures on a static, unmovable glass surface. Depending on signal amplitude and frequency, these textures are percieved as either vibrations or friction.</p>
</div>

<a href="teslatouch/hi-res/tt-handheldhd.jpg"><img src="teslatouch/tt-handheld.jpg"></a>
<div id="pjimagecap-new">
<p>TeslaTouch can enhance touch screen interaction on handheld devices.</p>
</div>

<a href="teslatouch/hi-res/tt-tablehd.jpg"><img src="teslatouch/tt-table.jpg"></a>
<div id="pjimagecap-new">
<p>An important property of TeslaTouch is that it can be provide tactile feedback for very large touch screens, such as interactive tables and walls.</p>
</div>

<a href="teslatouch/hi-res/tt-tablesetup.jpg"><img src="teslatouch/t-table-setup.png" width="450" height="298"></a>
<div id="pjimagecap-new">
<p>TeslaTouch can be combined with various input techniques, such as diffused IR illumination sensing approach that is often used for interactive tables.</p></div>

<a href="teslatouch/hi-res/tt-icons-dragging.jpg"><img src="teslatouch/tt-icons.jpg" width="450" height="300"></a>
<div id="pjimagecap-new">
<p>Traditional  icons such as files and folders can be enhanced with variable levels of tactile friction. That would allow to <em>feel</em> thier properties such as file size or interaction with other icons such as in drag-and-drop operations.</p>
</div>

<a href="teslatouch/hi-res/tt-constraints-large.jpg"><img src="teslatouch/tt-constraints.jpg" width="450" height="305"></a>
<div id="pjimagecap-new">
<p>Drawing interactions, such as objects positioning can be enhanced with tactile feedback, e.g.   users  could feel underlying alignment grids with thier fingers.</p>
</div>

<a href="teslatouch/hi-res/tt-textures-hr.jpg"><img src="teslatouch/tt-textures.jpg"></a>
<div id="pjimagecap-new">
<p>TeslaTouch can simulate a feeling of textures and materials, such as glass-smooth, or rough like corderoy or irregular like sand. The feeling of textures can be experienced  implicetely by sliding fingers over thier images.</p>
</div>

<a href="teslatouch/hi-res/tt-racing_hr.jpg"><img src="teslatouch/tt-racing.jpg" width="450" height="300"></a>
<div id="pjimagecap-new">
<p> Textures  can be experienced <em>explicitely</em> by simulating friction between objects, e.g. a user could feel a virtual pavement  while dragging a car along it.</p>
</div>


<a href="teslatouch/hi-res/paint-erasing.jpg"><img src="teslatouch/tt-paint-erasing.jpg" width="450" height="305"></a>
<div id="pjimagecap-new">
<p> TeslaTouch can  provide  tactile feedback on the degree of image modifications in painting applications. For example, in erasing task the tactile sensation would decreases as pixels are being progressively removed from the image.</p>
</div>

<a href="teslatouch/hi-res/tt-bi-manual-drawing-hr.jpg"><img src="teslatouch/tt-bi-manual-drawing.jpg" width="450" height="300"></a>
<div id="pjimagecap-new">
<p>TeslaTouch can provide tactile feedback for multitouch  gestures  that do not require using multiple fingers/hands at the same time. In drawing, one hand orients canvas, while another hand performs writing or drawing. In this case, TeslaTouch can provide unique tactile feedback to both hands.</p>
</div>

<a href="teslatouch/hi-res/tt-piemenu-hr.jpg"><img src="teslatouch/tt-piemenu.jpg" width="450" height="300"></a>
<div id="pjimagecap-new">
<p>The user selects from the pie menue using an achoring gesture with the thumb remaining static and the pointing finger sliding on the surface of display and therefore percieving tactile feedback.</p>
</div>


<a href="teslatouch/hi-res/tactile-ar.jpg"><img src="teslatouch/tt-ar.jpg" width="450" height="283"></a>
<div id="pjimagecap-new">
<p>TeslaTouch allows to map outputs from a broad vareity of sensors, such as camera, to tactile feedback.</p>
</div>

