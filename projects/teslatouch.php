<h2 align="left">What is TeslaTouch?</h2>
<p><em>TeslaTouch</em> is a new tactile interface technology that infuses finger-driven touch interfaces  with physical, tactile feedback. It is based on the electrovibration  principle, which can programmatically vary the friction between sliding fingers  and a touch panel. As the result, <em>TeslaTouch</em> technology requires no moving parts and therefore it is inexpensive, lightweight, requires little power and can provide tactile sensation on touch panels of any size and shape This is unlike most of the existing tactile feedback technologies, which use  mechanical devices to provide tactile sensation, such as vibration motors.</p>
<p>When combined with an interactive graphical  display, TeslaTouch enables the design of   interfaces that allow the user to feel virtual elements and their  properties on a touch screen. For example, when dragging a file, the level  of friction could convey the file size; feeling textures and materials  could be explored with fingers; objects could be felt &ldquo;snapping&rdquo; into  place or perhaps with a quick &ldquo;rub&rdquo; of email application&rsquo;s icon, you  could sense how many emails are unread. These are just a few ideas where this technolgy can be effectively used.</p>
